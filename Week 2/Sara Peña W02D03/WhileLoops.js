console.log("   while loop================");

//WHILE LOOP
var i=1;
while(i<=10){
    console.log(i);
    i++;
}

console.log("   Do While Loop=================");

//DO WHILE LOOP used in video games when they loop waiting for you to "START/PAUSE/PLAY" etc.

var i=1;
do{
    console.log(i);
    i++;
}while (i<=10);